package com.kosmo.shoong.service.pack;

public class PackGalleryDTO {
	private String pictureNo;
	private String packJoinNo;
	private String pictureName;

	public String getPictureNo() {
		return pictureNo;
	}
	public void setPictureNo(String pictureNo) {
		this.pictureNo = pictureNo;
	}
	public String getPackJoinNo() {
		return packJoinNo;
	}
	public void setPackJoinNo(String packJoinNo) {
		this.packJoinNo = packJoinNo;
	}
	public String getPictureName() {
		return pictureName;
	}
	public void setPictureName(String pictureName) {
		this.pictureName = pictureName;
	}
}
